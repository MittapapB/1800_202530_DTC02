document.addEventListener("DOMContentLoaded", () => {
  const tableView = document.getElementById("table-view");
  const listView = document.getElementById("list-view");
  const btnTable = document.getElementById("btn-table");
  const btnList = document.getElementById("btn-list");

  tableView.classList.remove("hidden");
  listView.classList.add("hidden");
  btnTable.classList.add("bg-primary");
  btnList.classList.add("bg-accent-green");

  btnTable.addEventListener("click", () => {
    tableView.classList.remove("hidden");
    listView.classList.add("hidden");
    btnTable.classList.add("bg-primary");
    btnTable.classList.remove("bg-accent-green");
    btnList.classList.remove("bg-primary");
    btnList.classList.add("bg-accent-green");
  });

  btnList.addEventListener("click", () => {
    tableView.classList.add("hidden");
    listView.classList.remove("hidden");
    btnTable.classList.remove("bg-primary");
    btnTable.classList.add("bg-accent-green");
    btnList.classList.add("bg-primary");
    btnList.classList.remove("bg-accent-green");
  });
});
